s1={1,2,3,4,5}
s2={4,5,6,7,8}

#union - | ,union()
print("1. Union = ",s1|s2)

#Intersection - & , intersection
print("2. Intersection = ",s1&s2)

#difference- difference,-
print("3. Difference = ",s1-s2)

#symmetric difference- ^,symmetric_difference
print("4. symmetric difference = ",s1^s2)
